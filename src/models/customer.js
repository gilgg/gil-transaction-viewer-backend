const mongoose = require("mongoose");

const customerSchema = mongoose.Schema({
  _id: {
    type: String,
    required: true,
    trim: true,
  },
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },
  gender: {
    type: String,
    required: true,
    trim: true,
  },
  country: {
    type: String,
    required: true,
    trim: true,
  },
  city: {
    type: String,
    required: true,
    trim: true,
  },
  street: {
    type: String,
    required: true,
    trim: true,
  },
  phone: {
    type: String,
    required: true,
    trim: true,
  },
});

customerSchema.virtual("transactions", {
  ref: "Transactions",
  localField: "_id",
  foreignField: "customer",
});

const Customer = mongoose.model("Customer", customerSchema);
module.exports = Customer;
